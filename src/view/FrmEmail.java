    public void preencher() {

        Properties properties = new Properties();

        try {
            //Setamos o arquivo que vai ser lido
            FileInputStream fis = new FileInputStream("C:\\BackupPostgresql\\config\\dados.properties");
            //metodo load faz a leitura atraves do objeto fis
            properties.load(fis);
            //Captura o valor da propriedade, atraves do nome da propriedade(Key           

            txtHost.setText(properties.getProperty("host"));
            txtPorta.setText(properties.getProperty("port"));
            txtSenha.setText(properties.getProperty("senha"));
            txtEmail.setText(properties.getProperty("email"));
            txtEmailContador.setText(properties.getProperty("emailcontador"));
            txtAssunto.setText(properties.getProperty("assunto"));
            txtMensagemIni.setText(properties.getProperty("mensagemini"));
            txtMensagemFim.setText(properties.getProperty("mensagemfim"));
            //txtCaminho.setText(properties.getProperty("caminho"));

        } catch (IOException e) {
           JOptionPane.showMessageDialog(rootPane, "Ocorreu um erro ao tentar preencher as informações. \nERRO"+e,"ERRO:",JOptionPane.ERROR_MESSAGE);
        }
    }

