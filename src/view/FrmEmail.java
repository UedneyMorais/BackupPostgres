    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        // TODO add your handling code here:
        try {
            //Cria um objeto da classe java.util.Properties
            Properties properties = new Properties();

            //setando as propriedades(key) e os seus valores(value)
            properties.setProperty("host", txtHost.getText());
            properties.setProperty("port", txtPorta.getText());
            properties.setProperty("senha", hash.HashBase64(txtSenha.getText()));
            properties.setProperty("email", txtEmail.getText());
            properties.setProperty("emailcontador", txtEmailContador.getText());
            properties.setProperty("assunto", txtAssunto.getText());
            properties.setProperty("mensagemini", txtMensagemIni.getText());
            properties.setProperty("mensagemfim", txtMensagemFim.getText());
            //properties.setProperty("caminho", txtCaminho.getText());

            //Criamos um objeto FileOutputStream
            FileOutputStream fos = new FileOutputStream("C:\\BackupPostgresql\\config\\dados.properties");
            //grava os dados no arquivo
            properties.store(fos, "ARQUIVO DE CONFIGURAÇÃO:");
            //fecha o arquivo
            fos.close();
            JOptionPane.showMessageDialog(rootPane, "Dados salvos com sucesso.","SUCESSO:",JOptionPane.INFORMATION_MESSAGE);
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(rootPane, "Ocorreu um erro ao tentar salvar as informações. \nERRO"+ex,"ERRO:",JOptionPane.ERROR_MESSAGE);
            ex.printStackTrace();
        }
        //Dados.setMessage(txtEmail.getText());
        bloqueaCampos();
        btnEditar.setEnabled(true);
    }//GEN-LAST:event_btnSalvarActionPerformed

